/**
kotlin Program to Find the Size of int, float, double, and char
 */
fun main() {
    val intSize = Int.SIZE_BYTES
    val floatSize = Float.SIZE_BYTES
    val doubleSize = Double.SIZE_BYTES
    val charSize = Char.SIZE_BYTES

    println("Size of Int: $intSize bytes")
    println("Size of Float: $floatSize bytes")
    println("Size of Double: $doubleSize bytes")
    println("Size of Char: $charSize bytes")
}
/**
Size of Int: 4 bytes
Size of Float: 4 bytes
Size of Double: 8 bytes
Size of Char: 2 bytes
 */
 