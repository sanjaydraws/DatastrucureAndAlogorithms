// CPP Program to Print an Integer Entered By the User


#include <iostream>
using namespace std;

int main() {
    int userInteger;

    // Prompting the user to enter an integer
    cout << "Enter an integer: ";

    // Reading the user's input
    cin >> userInteger;

    // Printing the entered integer
    cout << "You entered: " << userInteger << endl;

    return 0;
}
